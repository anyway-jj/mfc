void init_OLED(void) {
  write_i(0xAE); /*display off*/
  write_i(0x00); /*set lower column address*/
  write_i(0x10); /*set higher column address*/
  write_i(0x00); /*set display start line*/
  write_i(0xB0); /*set page address*/
  write_i(0x81); /*contract control*/
  write_i(0xff); /*128*/
  write_i(0xA1); /*set segment remap*/
  write_i(0xA6); /*normal / reverse*/
  write_i(0xA8); /*multiplex ratio*/
  write_i(0x1F); /*duty = 1/32*/
  write_i(0xC8); /*Com scan direction*/
  write_i(0xD3); /*set display offset*/
  write_i(0x00);
  write_i(0xD5); /*set osc division*/
  write_i(0x80);
  write_i(0xD9); /*set pre-charge period*/
  write_i(0x1f);
  write_i(0xDA); /*set COM pins*/
  write_i(0x00);
  write_i(0xdb); /*set vcomh*/
  write_i(0x40);
  write_i(0x8d); /*set charge pump enable*/
  write_i(0x14);
  write_i(0x20);
  write_i(0x00); //HORIZONTAL ADDRESING MODE
  //write_i(0x01); //VERTICAL ADDRESING MODE
  write_i(0x21);
  write_i(0);
  write_i(127);
  write_i(0x22);
  write_i(0);
  write_i(3);
  write_i(0xAF); /*display ON*/
}


void write_i(uint8_t ins) {
  I2C1->CR1 |= I2C_CR1_START;
  while ( ((I2C1->SR1 & I2C_SR1_SB) != I2C_SR1_SB));
  I2C1->SR1;
  send_addr(0x78);
  write_w(0x00);
  write_w(ins);
  first_data = 1;
  while ( (I2C1->SR1 & I2C_SR1_TXE) != I2C_SR1_TXE);
  I2C1->CR1 |= I2C_CR1_STOP;
}

void write_d(uint8_t ins) {
  I2C1->CR1 |= I2C_CR1_START;
  while ( ((I2C1->SR1 & I2C_SR1_SB) != I2C_SR1_SB));
  I2C1->SR1;
  send_addr(0x78);
  write_w(0x40);
  write_w(ins);
  first_data = 1;
  while ( (I2C1->SR1 & I2C_SR1_TXE) != I2C_SR1_TXE);
  I2C1->CR1 |= I2C_CR1_STOP;
}

void send_addr(uint8_t addr) {
  I2C1->DR = addr;
  while ( (I2C1->SR1 & I2C_SR1_ADDR) != I2C_SR1_ADDR);
  I2C1->SR1;
  I2C1->SR2;
}

void write_w(uint8_t word) {
  while ( (I2C1->SR1 & I2C_SR1_TXE) != I2C_SR1_TXE);
  I2C1->DR = word;
  if (first_data == 1) {
    first_data = 0;
	while ( (I2C1->SR1 & I2C_SR1_BTF) != I2C_SR1_BTF);
  }
}

